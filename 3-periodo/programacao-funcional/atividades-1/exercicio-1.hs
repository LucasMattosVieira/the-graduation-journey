-- 1 + 2 * 3 -> Ele calcula corretamente sem a necessidade da colocação de parênteses para especificar o que deve ser feito primeiro. O resultado é 7.

    print(1 + 2 * 3) >>

-- 5 ^ 3 -> Ele calcula a potência tendo como resultado um número inteiro. O resultado é 125.

    print(5 ^ 3) >>

-- 5 ** 3 -> Ele calcula a potência tendo como resultado um número decimal. O resultado é 125.0.

    print(5 ** 3) >>

-- 5 / 3 -> Ele calcula a divisão com casas decimais. O resultado é aproximadamente 1,667.

    print(5 / 3) >>

-- div 5 3 -> Ele calcula a divisão inteira. O resultado é 1.

    print(div 5 3) >>

-- mod 5 3 -> Ele calcula o resto da divisão do primeiro número pelo segundo número (módulo). O resultado é 2.

    print(mod 5 3) >>

-- 5 < 3 -> Ele retorna True ou False de acordo com a comparação. O resultado é False.

    print(5 < 3) >>

-- mod 5 3 < 2 -> Ele retorna True ou False de acordo com a comparação. O resultado é False.

    print(mod 5 3 < 2) >>

-- mod 5 3 == 2 -> Ele retorna True ou False de acordo com a comparação. O resultado é True.

    print(mod 5 3 == 2) >>

-- sqrt 81 -> Ele calcula a raiz quadrada retornando um número decimal. O resultado é 9.0.

    print(sqrt 81) >>

-- logBase 2 1024 -> Ele calcula logaritmo retornando um número decimal. O resultado é 10.0.

    print(logBase 2 1024) >>

-- floor 5.7 -> Ele arredonda o número pra baixo. O resultado é 5.

    print(floor 5.7) >>

-- ceiling 5.7 -> Ele arredonda o número pra cima . O resultado é 6.

    print(ceiling 5.7) >>

-- abs (-5) -> Ele calcula . O resultado é 7.

    print(abs (-5)) >>

-- min 6 7 -> Ele retorna o valor absoluto. O resultado é 5.

    print(min 6 7) >>

-- max 6 7 -> Ele retorna qual é o menor dos valores. O resultado é 6.

    print(max 6 7) >>

-- sin (pi/2) -> Ele calcula o seno retornando un número decimal. O resultado é 1.0.

    print(sin (pi/2)) >>

-- sum [1..5] -> Ele calcula a soma de uma lista de números. O resultado é 15.

    print(sum [1..5]) >>

-- not True -> O not nega o valor seguinte. O resultado é False.

    print(not True) >>

-- True && False -> Verifica se os dois argumentos são verdadeiros. O resultado é False.

    print(True && False)
